static uint32_t last_update = 0;
static uint8_t  hours, minutes, seconds;
void            matrix_scan_user(void) {
    uint32_t now = timer_read32();
    if (now - last_update > 500) {
        get_time(&hours, &minutes, &seconds);
        last_update = now;
        // dprintf("dpl_mode %d\n", dpl_mode);
    }
    if (dpl_adv) {
        if (dpl_mode == 2)
            dpl_mode = 0;
        else
            dpl_mode++;
    };
}

